package com.baseball.app.som;

public class SomDTO {
	
	
	private Long somNum;
	private Long seatId;
	private Long matchNum;
	private String somStatus;
	
	
	
	public Long getSomNum() {
		return somNum;
	}
	public void setSomNum(Long somNum) {
		this.somNum = somNum;
	}
	public Long getSeatId() {
		return seatId;
	}
	public void setSeatId(Long seatId) {
		this.seatId = seatId;
	}
	public Long getMatchNum() {
		return matchNum;
	}
	public void setMatchNum(Long matchNum) {
		this.matchNum = matchNum;
	}
	public String getSomStatus() {
		return somStatus;
	}
	public void setSomStatus(String somStatus) {
		this.somStatus = somStatus;
	}
	
	

}
