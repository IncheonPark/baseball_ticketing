package com.baseball.app.tickets;

public class TicketDTO {

	
	private Long matchNum;
	private Long seatNum;
	private Long ticketNum;
	private String userId;
	private String somStatus;
	
	
	
	
	
	public Long getMatchNum() {
		return matchNum;
	}
	public void setMatchNum(Long matchNum) {
		this.matchNum = matchNum;
	}
	public Long getSeatNum() {
		return seatNum;
	}
	public void setSeatNum(Long seatNum) {
		this.seatNum = seatNum;
	}
	public Long getTicketNum() {
		return ticketNum;
	}
	public void setTicketNum(Long ticketNum) {
		this.ticketNum = ticketNum;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getSomStatus() {
		return somStatus;
	}
	public void setSomStatus(String somStatus) {
		this.somStatus = somStatus;
	}
	
	
	

	
	
	
	
	
	
}
